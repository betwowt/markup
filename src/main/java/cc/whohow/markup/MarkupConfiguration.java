package cc.whohow.markup;

public class MarkupConfiguration {
    private String git;
    private int port = 80;

    public String getGit() {
        return git;
    }

    public void setGit(String git) {
        this.git = git;
    }

    public int getPort() {
        return port;
    }

    public void setPort(int port) {
        this.port = port;
    }
}
